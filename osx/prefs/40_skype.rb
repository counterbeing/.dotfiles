# Disable creepy skype emoji
`defaults write ~/Library/Preferences/com.skype.skype SKEmoticonChatStyle 0`

# Disable spellcheck/autocorrect
`defaults write ~/Library/Preferences/com.skype.skype SKChatInputAutomaticSpellingCorrection 0`
`defaults write ~/Library/Preferences/com.skype.skype SKChatInputContinuousSpellChecking 0`

